public class CoinIdMappingPolicy: NSEntityMigrationPolicy {
    public override func createDestinationInstances(forSource sInstance: NSManagedObject,
                                                    in mapping: NSEntityMapping,
                                                    manager: NSMigrationManager) throws {
        if sInstance.entity.name == "Alert" {
            let alertSentDate: Date? = sInstance.primitiveValue(forKey: "alertSentDate") as? Date
            let coinSymbol: String = sInstance.primitiveValue(forKey: "coinSymbol") as! String
            let currency: String = sInstance.primitiveValue(forKey: "currency") as! String
            let isEnabled: Bool = sInstance.primitiveValue(forKey: "isEnabled") as! Bool
            let isPriceLower: Bool = sInstance.primitiveValue(forKey: "isPriceLower") as! Bool
            let price: Double = sInstance.primitiveValue(forKey: "price") as! Double
            
            let alert = NSEntityDescription.insertNewObject(forEntityName: "Alert", into: manager.destinationContext)
            alert.setValue(alertSentDate, forKey: "alertSentDate")
            alert.setValue(coinSymbol, forKey: "coinSymbol")
            alert.setValue(currency, forKey: "currency")
            alert.setValue(isEnabled, forKey: "isEnabled")
            alert.setValue(isPriceLower, forKey: "isPriceLower")
            alert.setValue(price, forKey: "price")
            
            let coinId = self.getCoinIdForCoinSymbol(coinSymbol: coinSymbol)
            alert.setValue(coinId, forKey: "coinId")
        }
        
        if sInstance.entity.name == "ExchangeItem" {
            let amount: Double = sInstance.primitiveValue(forKey: "amount") as! Double
            let coinSymbol: String = sInstance.primitiveValue(forKey: "coinSymbol") as! String
            let currency: String = sInstance.primitiveValue(forKey: "currency") as! String
            
            let alert = NSEntityDescription.insertNewObject(forEntityName: "ExchangeItem", into: manager.destinationContext)
            alert.setValue(amount, forKey: "amount")
            alert.setValue(coinSymbol, forKey: "coinSymbol")
            alert.setValue(currency, forKey: "currency")
            
            let coinId = self.getCoinIdForCoinSymbol(coinSymbol: coinSymbol)
            alert.setValue(coinId, forKey: "coinId")
        }
        
        if sInstance.entity.name == "Favourite" {
            let coinSymbol: String = sInstance.primitiveValue(forKey: "coinSymbol") as! String
            
            let alert = NSEntityDescription.insertNewObject(forEntityName: "Favourite", into: manager.destinationContext)
            
            let coinId = self.getCoinIdForCoinSymbol(coinSymbol: coinSymbol)
            alert.setValue(coinId, forKey: "coinId")
        }
    }
    
